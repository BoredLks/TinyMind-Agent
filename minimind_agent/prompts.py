"""集中存放各节点 / 各专家 Agent 的提示词。

注意：MiniMind 是约 64M 的超小模型，对“长指令 + 复杂 JSON”泛化能力有限。因此提示词刻意保持
简短、贴近其 SFT 语料风格（中文、口语化指令）。规划阶段的 JSON 仅作“尽力而为”，真正兜底的是
``planner`` 里的确定性关键词分类器。
"""

from __future__ import annotations  # 启用延迟注解求值（本文件其实只有字符串常量）

# ---------------------------------------------------------------------------
# 规划主管（planner）
# ---------------------------------------------------------------------------
PLANNER_SYSTEM = """你是一个任务规划助手。你手下有三个专家：
- file：负责移动文件
- code：负责编写简单的 Python 代码
- story：负责编写小故事

请把用户任务拆解成有序的步骤，每个步骤交给其中一个专家。
只输出一个 JSON 数组，每个元素形如 {"agent": "code", "instruction": "具体要做的事"}。
不要输出多余解释。"""  # planner 的 system 提示：说明三个专家 + 要求只输出 JSON 计划数组

PLANNER_USER = """用户任务：{task}

请输出 JSON 计划数组。"""  # planner 的 user 模板：{task} 处填入用户原始任务


# ---------------------------------------------------------------------------
# code 专家：写 Python 代码
# ---------------------------------------------------------------------------
CODE_SYSTEM = """你是一个 Python 程序员。请根据要求编写简洁、可运行的 Python 代码。
只输出代码本身，用 ```python 代码块包裹，不要输出多余的解释。"""  # 让模型只吐 ```python 代码块，便于抽取

CODE_USER = """请用 Python 完成下面的需求：
{instruction}"""  # code 的 user 模板：{instruction} 处填入该步骤的指令


# ---------------------------------------------------------------------------
# story 专家：写小故事
# ---------------------------------------------------------------------------
STORY_SYSTEM = """你是一个会写故事的作家。请根据要求写一篇结构完整、语言流畅的小故事。"""  # story 的 system 提示

STORY_USER = """请写一篇小故事，要求如下：
{instruction}"""  # story 的 user 模板：{instruction} 处填入该步骤的指令


# ---------------------------------------------------------------------------
# file 专家：当正则无法抽取“源/目标”时，让模型兜底抽取
# ---------------------------------------------------------------------------
FILE_EXTRACT_SYSTEM = """你负责从一句话里抽取“要移动的源文件”和“目标位置”。
只输出 JSON：{"source": "源文件路径", "destination": "目标位置"}，不要输出多余内容。"""  # 兜底抽取的 system 提示（要求只输出 JSON）

FILE_EXTRACT_USER = """指令：{instruction}

请输出 JSON。"""  # 兜底抽取的 user 模板：{instruction} 处填入移动指令
